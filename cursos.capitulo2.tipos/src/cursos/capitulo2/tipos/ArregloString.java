package cursos.capitulo2.tipos;


public class ArregloString 
{
    public static void main(String[] args) 
    {
       //Crear un arreglo que contenga las frutas que me gusten
        String []frutas={"manzana","uva","mango","durazno","sandia","melon"};
        //crear el ciclo for para iterar tus frutas.
        for(String b:frutas)
        {
            System.out.println(b);
        }
    }
}
